package Holders;

/**
 *
 * @author danushka
 */
public class PayReceiptHolder {
    
    private String receiptno;
    private String center;
    private String cashier;
    private String memberno;
    private String membername;
    private String contractno;
    private String loan;
    private String paid;
    private String arreas;
    private String prebalance;
    private String newbalance;

    public String getReceiptno() {
        return receiptno;
    }

    public void setReceiptno(String receiptno) {
        this.receiptno = receiptno;
    }

    public String getCenter() {
        return center;
    }

    public void setCenter(String center) {
        this.center = center;
    }

    public String getCashier() {
        return cashier;
    }

    public void setCashier(String cashier) {
        this.cashier = cashier;
    }

    public String getMemberno() {
        return memberno;
    }

    public void setMemberno(String memberno) {
        this.memberno = memberno;
    }

    public String getMembername() {
        return membername;
    }

    public void setMembername(String membername) {
        this.membername = membername;
    }

    public String getContractno() {
        return contractno;
    }

    public void setContractno(String contractno) {
        this.contractno = contractno;
    }

    public String getLoan() {
        return loan;
    }

    public void setLoan(String loan) {
        this.loan = loan;
    }

    public String getPaid() {
        return paid;
    }

    public void setPaid(String paid) {
        this.paid = paid;
    }

    public String getArreas() {
        return arreas;
    }

    public void setArreas(String arreas) {
        this.arreas = arreas;
    }

    public String getPrebalance() {
        return prebalance;
    }

    public void setPrebalance(String prebalance) {
        this.prebalance = prebalance;
    }

    public String getNewbalance() {
        return newbalance;
    }

    public void setNewbalance(String newbalance) {
        this.newbalance = newbalance;
    }
    
    
    
}
